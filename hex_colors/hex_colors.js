function ColorFinder(hex_color){
  if(hex_color === "#F0F8FF"){
    console.log("AliceBlue")
  }else if(hex_color === "#FAEBD7"){
    console.log("AntiqueWhite")
  }else if(hex_color === "#00FFFF"){
    console.log("Aqua")
  };
};

// AliceBlue #F0F8FF Shades
ColorFinder("#F0F8FF");
// AntiqueWhite  #FAEBD7 Shades
ColorFinder("#FAEBD7");
// Aqua  #00FFFF Shades
ColorFinder("#00FFFF");
